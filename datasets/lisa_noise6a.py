"""Loader for the realistic ``noise6a`` LISA TDI dataset.

``noise6a_tdi.h5`` (generated by the ``lisa_datagen`` project) is a compact
one-year XYZ gen-2 TDI dataset (``dt = 2 s``) containing stationary instrument
noise, an *annually modulated* Galactic confusion foreground, and one bright
resolved Galactic binary near 3.21 mHz. We transform to the noise-orthogonal
A/E/T channels and band-limit to the confusion band so the time-varying PSD can
be estimated on a tractable WDM grid.
"""

from __future__ import annotations

import numpy as np

# Noise-orthogonal TDI combination (Prince et al. 2002), matching the lisa_datagen
# convention: A = (-X + Z)/sqrt2, E = (X - 2Y + Z)/sqrt6, T = (X + Y + Z)/sqrt3.
_ISQ2, _ISQ6, _ISQ3 = 1.0 / np.sqrt(2.0), 1.0 / np.sqrt(6.0), 1.0 / np.sqrt(3.0)


def xyz_to_aet(x: np.ndarray, y: np.ndarray, z: np.ndarray) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Transform X/Y/Z TDI to the orthogonal A/E/T channels."""
    a = -_ISQ2 * x + _ISQ2 * z
    e = _ISQ6 * x - 2.0 * _ISQ6 * y + _ISQ6 * z
    t = _ISQ3 * x + _ISQ3 * y + _ISQ3 * z
    return a, e, t


def _band_limit_decimate(series: np.ndarray, n_out: int) -> np.ndarray:
    """Ideal-lowpass decimation to ``n_out`` samples via rFFT truncation."""
    spectrum = np.fft.rfft(series)
    return np.fft.irfft(spectrum[: n_out // 2 + 1], n=n_out)


def load_noise6a_aet(
    path: str, *, nt: int, nf: int, normalize: bool = True
) -> dict[str, object]:
    """Load ``noise6a``, transform to A/E/T, and band-limit to ``nt*nf`` samples.

    Args:
        path: Path to ``noise6a_tdi.h5``.
        nt: Number of WDM time bins (the output length is ``nt * nf``).
        nf: Number of WDM frequency channels.
        normalize: Divide each channel by its standard deviation (O(1) for the
            estimator); the time-varying *shape* of the PSD is preserved.

    Returns:
        Dict with ``A, E, T`` (decimated series), ``dt`` (new interval),
        ``gb_f0`` (Hz), ``duration``, and the per-channel ``scale`` factors.
    """
    import h5py

    with h5py.File(path, "r") as f:
        x = np.asarray(f["X2"][:], dtype=float)
        y = np.asarray(f["Y2"][:], dtype=float)
        z = np.asarray(f["Z2"][:], dtype=float)
        duration = float(f.attrs["duration"])
        gb_f0 = float(f.attrs.get("bright_gb_f0_hz", np.nan))

    a, e, t = xyz_to_aet(x, y, z)
    n_out = nt * nf
    dt_out = duration / n_out
    channels, scales = {}, {}
    for name, series in (("A", a), ("E", e), ("T", t)):
        dec = _band_limit_decimate(series, n_out)
        scale = float(np.std(dec)) if normalize else 1.0
        channels[name] = dec / scale
        scales[name] = scale

    return {
        "A": channels["A"], "E": channels["E"], "T": channels["T"],
        "dt": dt_out, "n_total": n_out, "duration": duration,
        "gb_f0": gb_f0, "scale": scales,
        "nyquist_hz": 0.5 / dt_out,
    }
